// Adapted from code by Matt Walters https://www.mattwalters.net/posts/2018-03-28-hugo-and-lunr/

(function ($) {
  'use strict';

  $(document).ready(function () {
    
    // Get the search input and the results container
    const $searchInput = $('.td-search input');

    // Get the search page URL dynamically from Hugo's data attribute. This ensures paths like /devel/search/ or /7.0/search/ work automatically
    const searchPageUrl = $searchInput.data('search-page-url');

    // --- 1. EVENT HANDLERS ---
    $searchInput.on('change', (event) => {
      const query = $(event.target).val();
      if (query === '') return;

      // Comprobamos si el contenedor existe en este preciso instante
      const isSearchPage = $('#search-results-container').length > 0;

      if (!isSearchPage) {
        window.location.href = `${searchPageUrl}?q=${encodeURIComponent(query)}`;
      } else {
        renderResults(query);
      }
      $searchInput.blur();
    });

    // Prevent default form submission to avoid page reload
    $searchInput.closest('form').on('submit', () => false);

    //
    // Lunr
    //

    // --- 2. LUNR INDEX LOADING ---

    let idx = null;
    const resultDetails = new Map();

    // Request the JSON index file generated by Hugo
    $.ajax($searchInput.data('offline-search-index-json-src')).then((data) => {
      
      idx = lunr(function () {
        this.ref('ref');
        
        // Field weighting: Title is most important
        this.field('title', { boost: 5 });
        this.field('categories', { boost: 3 });
        this.field('tags', { boost: 3 });
        this.field('description', { boost: 2 });
        this.field('body');

        data.forEach((doc) => {
          this.add(doc);
          resultDetails.set(doc.ref, {
            title: doc.title,
            excerpt: doc.excerpt,
            // Clean up whitespace for cleaner snippets
            body: doc.body.replace(/\s+/g, ' '),
            description: doc.description
          });
        });
      });

      // AUTO-EXECUTE: Check if URL has a search parameter (coming from another page)
      const urlParams = new URLSearchParams(window.location.search);
      const queryParam = urlParams.get('q');
      if (queryParam) {
        $searchInput.val(queryParam);
        renderResults(queryParam);
      }
    });

    // --- 3. RENDERING LOGIC ---
    const renderResults = (searchQuery) => {
      const $status = $('#search-status');
      const $container = $('#search-results-container');
      const $template = $('#search-result-item-template');

      // Debug to see why it might stop
      if (idx === null) { console.log("Index is null"); return; }
      if ($container.length === 0) { console.log("Container not found"); return; }

      $container.empty();
      
      // THIS IS THE MISSING PART: The actual search
      const results = idx.query((q) => {
        const tokens = lunr.tokenizer(searchQuery.toLowerCase());
        tokens.forEach((token) => {
          const queryString = token.toString();
          q.term(queryString, { boost: 100 });
          q.term(queryString, { wildcard: lunr.Query.wildcard.TRAILING, boost: 10 });
          q.term(queryString, { editDistance: 2 });
        });
      }).slice(0, 20);

      // Status message
      if (results.length === 0) {
        $status.text(`No results found for "${searchQuery}"`);
        return;
      }
      $status.text(`Results for "${searchQuery}"`);

      // Iteration through results
      results.forEach((r) => {
        const doc = resultDetails.get(r.ref);
        if (!doc || $template.length === 0) return;

        // Clone template
        const $entry = $( $template[0].content.cloneNode(true) );
        
        // Fill data
        $entry.find('.result-url').text(r.ref);
        $entry.find('.result-link').attr('href', r.ref).html(highlightTextOnly(doc.title, searchQuery));
        $entry.find('.result-snippet').html(getHighlightedSnippet(doc.body, searchQuery, doc.description));
        
        $container.append($entry);
      });
    };

    // --- 4. HELPER FUNCTIONS FOR TEXT PROCESSING ---
    /**
     * Highlights query terms within a string without truncating it.
     * Useful for titles and short descriptions.
     */
    function highlightTextOnly(text, query) {
      if (!text) return "";
      const queryTrimmed = query.trim();
      const terms = queryTrimmed.toLowerCase().split(/\s+/).filter(t => t.length > 2);
      let highlighted = text;

      // 1. Highlight full phrase first to avoid nested tags
      const phraseRegex = new RegExp(`(${queryTrimmed})`, 'gi');
      highlighted = highlighted.replace(phraseRegex, '<span class="search-highlight">$1</span>');

      // 2. Highlight individual terms that aren't already highlighted
      terms.forEach(term => {
        const termRegex = new RegExp(`(?<![A-Za-z0-9])(${term})(?![A-Za-z0-9])`, 'gi');
        highlighted = highlighted.replace(termRegex, (match, p1, offset, string) => {
            // Simple check to avoid re-highlighting inside a <mark> tag
            const contextBefore = string.substring(Math.max(0, offset - 10), offset);
            if (contextBefore.includes('search-highlight')) return match;
            return `<span class="search-highlight">${match}</span>`;
        });
      });
      return highlighted;
    }

    /**
     * Extracts a relevant snippet from the body centered around the query.
     * Falls back to the description or start of body if term is not found.
     */
    function getHighlightedSnippet(body, query, description) {
      const bodyText = body || "";
      const descText = description || "";
      const queryLower = query.toLowerCase().trim();
      
      let index = bodyText.toLowerCase().indexOf(queryLower);
      let snippet = "";

      if (index !== -1) {
        // Priority 1: Phrase found in body - center snippet around it
        const start = Math.max(0, index - 100);
        const end = Math.min(bodyText.length, index + 400);
        snippet = (start > 0 ? "..." : "") + bodyText.substring(start, end) + "...";
      } else if (descText.trim().length > 0) {
        // Priority 2: Not in body, use provided description
        snippet = descText;
      } else {
        // Priority 3: Fallback to start of the document
        snippet = bodyText.substring(0, 180) + (bodyText.length > 180 ? "..." : "");
      }

      return highlightTextOnly(snippet, query);
    }

  });

})(jQuery);
